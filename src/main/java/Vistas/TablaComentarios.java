    private void cargarComentarios() {
        DefaultTableModel modelo = (DefaultTableModel) tablaClientes.getModel();
        modelo.setRowCount(0);

        List<Contacto> comentarios = contactoService.obtenerTodos();
        if (comentarios.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay comentarios registrados todav√≠a.");
            return;
        }

        for (Contacto c : comentarios) {
            modelo.addRow(new Object[]{
                c.getIdContacto(),
                c.getNombre(),
                c.getEmpresa(),
                c.getEmail(),
                c.getTelefono(),
                c.getServicio(),
                c.getMensaje(),
                c.getFechaEnvio()
            });
        }
    }

