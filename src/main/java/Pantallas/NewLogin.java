    private void validacion() {
        // Instancias de servicios/repositorios
        AuthService auth = new AuthService();
        ClienteRepositorio clienteRepo = new ClienteRepositorio();
        DuenoRepositorio duenoRepo = new DuenoRepositorio();

        System.out.println("===== VALIDACI√ìN DE DATOS EXISTENTES EN BD =====");

        try {
            // CLIENTES
            List<Cliente> clientes = clienteRepo.obtenerTodos();
            System.out.println("\nCLIENTES REGISTRADOS (" + clientes.size() + "):");
            if (clientes.isEmpty()) {
                System.out.println("  No hay clientes registrados.");
            } else {
                for (Cliente c : clientes) {
                    System.out.println("  - " + c.getNombreCliente() + " | " + c.getEmailCliente()+ " | " + c.getContrasenaCliente());
                }
            }

            // DUE√ëOS
            List<Dueno> duenos = duenoRepo.obtenerTodos();
            System.out.println("\nDUE√ëOS REGISTRADOS (" + duenos.size() + "):");
            if (duenos.isEmpty()) {
                System.out.println("  No hay due√±os registrados.");
            } else {
                for (Dueno d : duenos) {
                    System.out.println("  - " + d.getNombreDueno() + " | " + d.getEmailDueno()+ " | " + d.getContrasenaDueno());
                }
            }

            System.out.println("\n Conexion y validacion completadas correctamente.");

        } catch (Exception e) {
            System.err.println("‚ùå Error al obtener datos desde la base de datos: " + e.getMessage());
            e.printStackTrace();
        }
    }


    private void ACEPTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ACEPTARActionPerformed
        String email = txtCorreo.getText().trim().toLowerCase();
        String contrasena = new String(txtContrasena.getPassword()).trim();

        if (email.isEmpty() || contrasena.isEmpty()
                || email.equalsIgnoreCase("ingrese su correo")
                || contrasena.equalsIgnoreCase("contrase√±a")) {
            JOptionPane.showMessageDialog(this,
                    "Por favor, ingrese su correo y contrase√±a.",
                    "Advertencia",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        AuthService auth = new AuthService();
        ClienteRepositorio clienteRepo = new ClienteRepositorio();
        DuenoRepositorio duenoRepo = new DuenoRepositorio();

        // üîπ Validar Cliente
        if (auth.validarCredencialesCliente(email, contrasena)) {
            Cliente cliente = clienteRepo.verPorEmail(email);
            Session.iniciarSesionCliente(cliente);

            String rol = cliente.getRol(); // <- Aseg√∫rate que exista getRol() en la clase Cliente

            if (rol != null) {
                if (rol.equalsIgnoreCase("ROLE_ADMIN")) {
                    JOptionPane.showMessageDialog(this,
                            "Bienvenido Administrador: " + cliente.getNombreCliente(),
                            "Ingreso Exitoso",
                            JOptionPane.INFORMATION_MESSAGE);
                    new PantallaAdmin(cliente).setVisible(true);
                } else if (rol.equalsIgnoreCase("ROLE_USER")) {
                    JOptionPane.showMessageDialog(this,
                            "Bienvenido Cliente: " + cliente.getNombreCliente(),
                            "Ingreso Exitoso",
                            JOptionPane.INFORMATION_MESSAGE);
                    new PantallaCliente(cliente).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Rol desconocido: " + rol,
                            "Error de rol",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }

                this.dispose();
                return;
            } else {
                JOptionPane.showMessageDialog(this,
                        "El cliente no tiene un rol asignado.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }

        // üîπ Validar Due√±o
        if (auth.validarCredencialesDueno(email, contrasena)) {
            Dueno dueno = duenoRepo.verPorEmail(email);
            Session.iniciarSesionDueno(dueno);
            JOptionPane.showMessageDialog(this,
                    "Bienvenido Due√±o: " + dueno.getNombreDueno(),
                    "Ingreso Exitoso",
                    JOptionPane.INFORMATION_MESSAGE);
            new PantallaDueno(dueno).setVisible(true);
            this.dispose();
            return;
        }

        // ‚ùå Ninguna coincidencia
        JOptionPane.showMessageDialog(this,
                "Credenciales incorrectas o usuario no registrado.",
                "Error",
                JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_ACEPTARActionPerformed

