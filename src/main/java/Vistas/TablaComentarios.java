    private void btnPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPDFActionPerformed
        try {
            // Obtener los comentarios desde el servicio
            List<Contacto> comentarios = contactoService.obtenerTodos();

            if (comentarios.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay comentarios para exportar.");
                return;
            }

            // Crear el documento PDF
            com.itextpdf.text.Document documento = new com.itextpdf.text.Document();
            String ruta = System.getProperty("user.home") + "/Desktop/Comentarios.pdf";
            com.itextpdf.text.pdf.PdfWriter.getInstance(documento, new java.io.FileOutputStream(ruta));
            documento.open();

            // T√≠tulo del documento
            com.itextpdf.text.Paragraph titulo = new com.itextpdf.text.Paragraph("üìã Lista de Comentarios Registrados\n\n",
                    new com.itextpdf.text.Font(com.itextpdf.text.Font.FontFamily.HELVETICA, 18, com.itextpdf.text.Font.BOLD));
            titulo.setAlignment(com.itextpdf.text.Element.ALIGN_CENTER);
            documento.add(titulo);

            // Crear tabla PDF con encabezados
            com.itextpdf.text.pdf.PdfPTable tabla = new com.itextpdf.text.pdf.PdfPTable(8); // 8 columnas
            tabla.setWidthPercentage(100);

            // Encabezados
            String[] encabezados = {"ID", "Nombre", "Empresa", "Email", "Tel√©fono", "Servicio", "Mensaje", "Fecha Env√≠o"};
            for (String encabezado : encabezados) {
                com.itextpdf.text.pdf.PdfPCell celda = new com.itextpdf.text.pdf.PdfPCell(new com.itextpdf.text.Phrase(encabezado));
                celda.setBackgroundColor(com.itextpdf.text.BaseColor.LIGHT_GRAY);
                celda.setHorizontalAlignment(com.itextpdf.text.Element.ALIGN_CENTER);
                tabla.addCell(celda);
            }

            // Agregar filas con los datos
            for (Contacto c : comentarios) {
                tabla.addCell(String.valueOf(c.getIdContacto()));
                tabla.addCell(c.getNombre());
                tabla.addCell(c.getEmpresa());
                tabla.addCell(c.getEmail());
                tabla.addCell(c.getTelefono());
                tabla.addCell(c.getServicio());
                tabla.addCell(c.getMensaje());
                tabla.addCell(c.getFechaEnvio().toString());
            }

            documento.add(tabla);
            documento.close();

            JOptionPane.showMessageDialog(this, "PDF generado correctamente en el escritorio.", "√âxito", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al generar PDF: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPDFActionPerformed


