    private void configurarPlaceholders() {
        // Correo
        txtCorreo.setText("Ingrese su Correo");
        txtCorreo.setForeground(new java.awt.Color(150, 150, 150));

        txtCorreo.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent evt) {
                if (txtCorreo.getText().equals("Ingrese su Correo")) {
                    txtCorreo.setText("");
                    txtCorreo.setForeground(new java.awt.Color(255, 255, 255));
                }
            }

            @Override
            public void focusLost(java.awt.event.FocusEvent evt) {
                if (txtCorreo.getText().isEmpty()) {
                    txtCorreo.setText("Ingrese su Correo");
                    txtCorreo.setForeground(new java.awt.Color(150, 150, 150));
                }
            }
        });

        // Contrase√±a
        txtContrasena.setText("Contrase√±a");
        txtContrasena.setForeground(new java.awt.Color(150, 150, 150));
        txtContrasena.setEchoChar((char) 0);
        contrasenaPlaceholderActivo = true;
        contrasenaVisible = false;

        txtContrasena.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent evt) {
                if (contrasenaPlaceholderActivo) {
                    txtContrasena.setText("");
                    txtContrasena.setForeground(new java.awt.Color(255, 255, 255));
                    contrasenaPlaceholderActivo = false;
                    // Cuando gana foco, respeta estado del ojo
                    if (!contrasenaVisible) {
                        txtContrasena.setEchoChar('‚Ä¢');
                    } else {
                        txtContrasena.setEchoChar((char) 0);
                    }
                }
            }

            @Override
            public void focusLost(java.awt.event.FocusEvent evt) {
                if (String.valueOf(txtContrasena.getPassword()).isEmpty()) {
                    txtContrasena.setText("Contrase√±a");
                    txtContrasena.setForeground(new java.awt.Color(150, 150, 150));
                    txtContrasena.setEchoChar((char) 0);
                    contrasenaPlaceholderActivo = true;
                }
            }
        });

    }


